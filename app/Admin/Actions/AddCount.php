namespace App\Admin\Actions;

use Dcat\Admin\Grid\RowAction;
use Dcat\Admin\Grid;
use App\Admin\Model\Shop;

class AddCount extends RowAction
{
    public function title()
    {
        return '增加数量';
    }
    public function script()
    {

        $id =$this->row->id;
        return <<<JS

$(".add-count-{$id}").on('click', function () {
          var id = $id;
          var count = $(".add-count-{$id}").parents("tr").children("td").eq(6).text();
          var count_a = parseInt(count);
          var count_add = count_a + 1;
          $(".add-count-{$id}").parents("tr").children("td").eq(6).text(count_add);
          $.ajax({
                type: 'POST',
                url: '../admin/shopping/add/' + id,
                data: {'_token':'{{csrf_token()}}'},
                success: function (resp) {
                   Dcat.success('数量增加', null, {
    timeOut: 5000, // 5秒后自动消失
});
                },
                error: function (resp) {
                alert(resp.responseText)
                },
            });
    console.log($(this).data('id'));
    console.log(Dcat.grid.selected);
});
JS;

    }

    public function html()
    {
        // 获取当前行数据ID
        $id = $this->getKey();

        // 获取当前行数据的用户名
        $name = $this->row->name;

        // 这里需要添加一个class, 和上面script方法对应
        $this->setHtmlAttribute(['data-id' => $id, 'name' => $name, 'class' => 'add-count-'.$id]);
        return parent::html(); // TODO: Change the autogenerated stub
    }

}